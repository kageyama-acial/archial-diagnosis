function calcScore(a: string, b: string): number {
  if (a === b) return -1;
  let s = 0;
  for (let i = 0; i < 4; i++) {
    if (typeAxes[a][i] === typeAxes[b][i]) s += weights[i];
  }
  return s;
}

function toHearts(s: number): number {
  if (s === -1) return 4;
  if (s >= 7) return 5;
  if (s >= 5) return 4;
  if (s >= 3) return 3;
  if (s >= 1) return 2;
  return 0;
}

